uint64
sys_exit(void)
{
  int n;
  if(argint(0, &n) < 0)
    return -1;
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  if(argaddr(0, &p) < 0)
    return -1;
  return wait(p);
}

uint64
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0) 
    return -1;
  acquire(&tickslock); // 获取tickslock锁,为什么要获取锁？是因为ticks是全局变量，多个进程可能同时访问ticks
  ticks0 = ticks; // 记录当前时间
  while(ticks - ticks0 < n){   // 当前时间 - 记录时间 < 睡眠时间
    if(myproc()->killed){  // 如果进程被杀死
      release(&tickslock);  // 释放锁
      return -1;
    }
    sleep(&ticks, &tickslock);  // 睡眠 sleep函数的参数
  }
  release(&tickslock);   
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}
